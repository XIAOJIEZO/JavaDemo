public class Variable {
    public static void main(String[] args){
        int varibale;
        varibale = 10;

        System.out.println(varibale%3);     /*取余*/
        System.out.println(varibale/3);      /*取模，*/
        System.out.println(varibale/3.0);   /*除法*/
    }
}

/* int：基本数据类型，用来表示一个整数，取值范围-2^31~2^31-1*/