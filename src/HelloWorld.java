public class HelloWorld {       /*类：class  HelloWorld是类名   public class：告诉Java类名要与代码文件名一致  大括号内是类的内容*/
    public static void main(String[] args){
        System.out.println("Hello World !");
    }
}
/*  public static void main(String[] args):main方法的定义》告诉Java这是程序入口，也就是程序开始执行的地方。大括号内是方法的内容，又称方法体（method body），
    main方法最为特殊的一点是，它是Java程序的入口。
 */
